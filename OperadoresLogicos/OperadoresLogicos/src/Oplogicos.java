
public class Oplogicos {
    public static void main(String[] args) {
        System.out.println("Operadores logicos \n"
                + "");
        System.out.println("Operador AND \n");
        System.out.println(true+ "&" +true+ "=" +(true & true)+ "\n");
        System.out.println(false+ "&" +true+ "=" +(false & true)+ "\n");
        System.out.println(true+ "&" +false+ "=" +(true & false)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(false & false)+ "\n");
        System.out.println("Operador Logtico AND \n"
                + "");
        System.out.println(true+ "&" +true+ "=" +(true && true)+ "\n");
        System.out.println(false+ "&" +true+ "=" +(false && true)+ "\n");
        System.out.println(true+ "&" +false+ "=" +(true && false)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(false && false)+ "\n");
        System.out.println("Operador OR \n"
                + "");
        System.out.println(true+ "&" +true+ "=" +(true | true)+ "\n");
        System.out.println(false+ "&" +true+ "=" +(false | true)+ "\n");
        System.out.println(true+ "&" +false+ "=" +(true | false)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(false | false)+ "\n");
        System.out.println("Operador OR logico \n"
                + "");
        System.out.println(true+ "&" +true+ "=" +(true || true)+ "\n");
        System.out.println(false+ "&" +true+ "=" +(false || true)+ "\n");
        System.out.println(true+ "&" +false+ "=" +(true || false)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(false || false)+ "\n");
        System.out.println("Operador XOR Logico \n"
                + "");
        System.out.println(true+ "&" +true+ "=" +(true ^ true)+ "\n");
        System.out.println(false+ "&" +true+ "=" +(false ^ true)+ "\n");
        System.out.println(true+ "&" +false+ "=" +(true ^ false)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(false ^ false)+ "\n");
        System.out.println("Operador Not \n"
                + "");
        System.out.println(true+ "&" +true+ "=" +(!true)+ "\n");
        System.out.println(false+ "&" +false+ "=" +(!false)+ "\n");
    }
    
}
